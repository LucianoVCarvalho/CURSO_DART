void main(List<String> args) {
  //Aritimáticos
  int a = 7;
  int b = 3;

  int resultado = (a + b);

  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  //Lógicos
  bool c = true;
  bool d = false;

  print(c && d); // And / E
  print(c && !d);
  print(c || d); // Or / OU
  print(c ^ d); // Xor / OU Exclusivo
}
